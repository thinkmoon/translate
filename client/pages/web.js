  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    wx.showLoading({
      title: '加载中>>',
    })
    const _ts = this;
    //请求markdown文件，并转换为内容
    wx.request({
      url: options.url,
      header: {
        'content-type': 'application/x-www-form-urlencoded'
      },
      success: (res) => {
        let data = app.towxml.toJson(res.data, 'markdown');
        _ts.setData({
          article: data
        });
        wx.hideLoading()
      }
    });
  },

